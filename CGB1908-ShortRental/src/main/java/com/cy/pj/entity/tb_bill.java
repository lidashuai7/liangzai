package com.cy.pj.entity;

import java.util.Date;

public class tb_bill {

	//账单单id
	private Integer billId;
	//账单号，唯一
	private String billNumber;
	//账单日期
	private Date billDate;
	//结算单数
	private Integer billOne;
	//结算总额
	private Double money;
	//佣金
	private Double brokerage;
	//收入金额
	private Double income;
	//转账状态1.已转账2.未转账3，转账失败
	private Integer status;
	//到款日期
	private Date arrival_date;

	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public Double getBrokerage() {
		return brokerage;
	}
	public void setBrokerage(Double brokerage) {
		this.brokerage = brokerage;
	}
	public Double getIncome() {
		return income;
	}
	public void setIncome(Double income) {
		this.income = income;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public Date getArrival_date() {
		return arrival_date;
	}
	public void setArrival_date(Date arrival_date) {
		this.arrival_date = arrival_date;
	}
	public tb_bill() {
		super();
		// TODO Auto-generated constructor stub
	}
	public Integer getBillId() {
		return billId;
	}
	public void setBillId(Integer billId) {
		this.billId = billId;
	}
	public String getBillNumber() {
		return billNumber;
	}
	public void setBillNumber(String billNumber) {
		this.billNumber = billNumber;
	}
	public Date getBillDate() {
		return billDate;
	}
	public void setBillDate(Date billDate) {
		this.billDate = billDate;
	}
	public Integer getBillOne() {
		return billOne;
	}
	public void setBillOne(Integer billOne) {
		this.billOne = billOne;
	}
	public tb_bill(Integer billId, String billNumber, Date billDate, Integer billOne, Double money, Double brokerage,
			Double income, Integer status, Date arrival_date) {
		super();
		this.billId = billId;
		this.billNumber = billNumber;
		this.billDate = billDate;
		this.billOne = billOne;
		this.money = money;
		this.brokerage = brokerage;
		this.income = income;
		this.status = status;
		this.arrival_date = arrival_date;
	}
	@Override
	public String toString() {
		return "tb_bill [billId=" + billId + ", billNumber=" + billNumber + ", billDate=" + billDate + ", billOne="
				+ billOne + ", money=" + money + ", brokerage=" + brokerage + ", income=" + income + ", status="
				+ status + ", arrival_date=" + arrival_date + "]";
	}

	
	
	
}
