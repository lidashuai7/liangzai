package com.cy.pj.entity;

import java.util.Date;

public class tb_userinfo {

	
	private Integer userid;
	
	private String username;
	
	private String nickname;
	
	private String name;
	
	private String gender;
	
	private String mailbox;
	
	private String phonenumber;
	
	private String sparephonenumber;
	
	private Date createDate;
	
	private Date modifyDate;
	
	private String pwd;
	
	private Integer usertype;

	public Integer getUserid() {
		return userid;
	}

	public void setUserid(Integer userid) {
		this.userid = userid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public tb_userinfo() {
		super();
	}

	@Override
	public String toString() {
		return "tb_userinfo [userid=" + userid + ", username=" + username + ", nickname=" + nickname + ", name=" + name
				+ ", gender=" + gender + ", mailbox=" + mailbox + ", phonenumber=" + phonenumber + ", sparephonenumber="
				+ sparephonenumber + ", createDate=" + createDate + ", modifyDate=" + modifyDate + ", pwd=" + pwd
				+ ", usertype=" + usertype + "]";
	}

	public tb_userinfo(Integer userid, String username, String nickname, String name, String gender, String mailbox,
			String phonenumber, String sparephonenumber, Date createDate, Date modifyDate, String pwd,
			Integer usertype) {
		super();
		this.userid = userid;
		this.username = username;
		this.nickname = nickname;
		this.name = name;
		this.gender = gender;
		this.mailbox = mailbox;
		this.phonenumber = phonenumber;
		this.sparephonenumber = sparephonenumber;
		this.createDate = createDate;
		this.modifyDate = modifyDate;
		this.pwd = pwd;
		this.usertype = usertype;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getMailbox() {
		return mailbox;
	}

	public void setMailbox(String mailbox) {
		this.mailbox = mailbox;
	}

	public String getPhonenumber() {
		return phonenumber;
	}

	public void setPhonenumber(String phonenumber) {
		this.phonenumber = phonenumber;
	}

	public String getSparephonenumber() {
		return sparephonenumber;
	}

	public void setSparephonenumber(String sparephonenumber) {
		this.sparephonenumber = sparephonenumber;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public Date getModifyDate() {
		return modifyDate;
	}

	public void setModifyDate(Date modifyDate) {
		this.modifyDate = modifyDate;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public Integer getUsertype() {
		return usertype;
	}

	public void setUsertype(Integer usertype) {
		this.usertype = usertype;
	}
	
}
